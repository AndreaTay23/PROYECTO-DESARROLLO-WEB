package DTO;

import java.io.Serializable;

/**
 *
 * @author AndreaTay
 */
public class LibrosDTO implements Serializable{

  private Long id;
  private String titulo;
  private String autor;
  private String categoria;
  private String editorial;
  private String aniopub;
  private String idioma; 
  
    /**
     * @return the idLibro
     */
    public Long getId() {
        return id;
    }

    /**
     * @param id the idLibro to set
     */
    public void setId(Long id) {
        this.id = id;
    }

    /**
     * @return the titulo
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * @param titulo the titulo to set
     */
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    /**
     * @return the autor
     */
    public String getAutor() {
        return autor;
    }

    /**
     * @param autor the autor to set
     */
    public void setAutor(String autor) {
        this.autor = autor;
    }

    /**
     * @return the categoria
     */
    public String getCategoria() {
        return categoria;
    }

    /**
     * @param categoria the categoria to set
     */
    public void setCategoria(String categoria) {
        this.categoria = categoria;
    }

    /**
     * @return the editorial
     */
    public String getEditorial() {
        return editorial;
    }

    /**
     * @param editorial the editorial to set
     */
    public void setEditorial(String editorial) {
        this.editorial = editorial;
    }

    /**
     * @return the aniopub
     */
    public String getAniopub() {
        return aniopub;
    }

    /**
     * @param aniopub the aniopub to set
     */
    public void setAniopub(String aniopub) {
        this.aniopub = aniopub;
    }

    /**
     * @return the idioma
     */
    public String getIdioma() {
        return idioma;
    }

    /**
     * @param idioma the idioma to set
     */
    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }
 
}
